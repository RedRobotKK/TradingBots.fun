-- ── Helper function: generate a random invite code ───────────────────────────
-- Called by the Rust layer via SELECT generate_invite_code(); returns "TB-XXXXXXXX"

CREATE OR REPLACE FUNCTION generate_invite_code() RETURNS TEXT
LANGUAGE sql AS $$
    SELECT 'TB-' || upper(substring(encode(gen_random_bytes(5), 'hex') FROM 1 FOR 8));
$$;

-- ── Helper function: claim an invite code ────────────────────────────────────
-- Atomically increments uses_count and returns the invite_code row if valid.
-- Returns NULL if code not found, inactive, expired, or at max uses.

CREATE OR REPLACE FUNCTION claim_invite_code(p_code TEXT)
RETURNS TABLE (
    invite_id   UUID,
    campaign_id UUID,
    created_by  UUID
) LANGUAGE plpgsql AS $$
DECLARE
    v_id        UUID;
    v_campaign  UUID;
    v_creator   UUID;
BEGIN
    UPDATE invite_codes
    SET    uses_count = uses_count + 1
    WHERE  code       = upper(p_code)
      AND  is_active  = TRUE
      AND  (expires_at IS NULL OR expires_at > now())
      AND  uses_count < max_uses
    RETURNING id, campaign_id, created_by
    INTO v_id, v_campaign, v_creator;

    IF v_id IS NULL THEN
        RETURN;   -- empty result set = invalid / exhausted code
    END IF;

    RETURN QUERY SELECT v_id, v_campaign, v_creator;
END;
$$;

COMMENT ON FUNCTION claim_invite_code IS
    'Atomically validates and claims one use of an invite code; returns empty if invalid/exhausted';
